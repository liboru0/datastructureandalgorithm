package com.spider.datastructure.linkedlist.josepfu;

public class Boy {
	
	private int no; // 编号
	
	public Boy() {}

	public Boy(int no) {
		this.no = no;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	@Override
	public String toString() {
		return "Boy [no=" + no + "]";
	}
	
}
